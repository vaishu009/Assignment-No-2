/*
 Write a program to calculate the sum of first 10 natural number.
 */
public class add1To10 
{
    public static void main(String args[])
    {
        int i,sum=0;
        for(i=1;i<=10;i++)
        {
            sum=sum+i;
        }
        System.out.println("sum = "+sum);
    }
}
